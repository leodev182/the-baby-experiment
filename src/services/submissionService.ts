import {
  doc,
  setDoc,
  updateDoc,
  increment,
  arrayUnion,
  serverTimestamp,
} from "firebase/firestore";
import { db } from "../lib/firebase";
import { rateLimiter, RATE_LIMITS } from "../utils/rateLimiters";
import type { PredictionDraft } from "./localStorageService";
import { logger } from "../utils/logger";

/**
 * Env√≠a la predicci√≥n completa a Firebase con rate limiting
 * UNA SOLA VEZ al final de todo el flujo
 */
export async function submitPrediction(draft: PredictionDraft): Promise<void> {
  // ‚úÖ Verificar rate limit ANTES de hacer cualquier cosa
  if (
    !rateLimiter.isAllowed("submitPrediction", RATE_LIMITS.SUBMIT_PREDICTION)
  ) {
    logger.warn("‚ö†Ô∏è Rate limit alcanzado. Por favor espera unos segundos.");
    throw new Error(
      "Por favor espera unos segundos antes de enviar nuevamente"
    );
  }

  try {
    logger.log("üöÄ Iniciando env√≠o a Firebase...");
    logger.log("üì¶ Draft a enviar:", draft);

    // Validar que el draft tenga hip√≥tesis
    if (!draft.hypothesis) {
      throw new Error("No se puede enviar sin hip√≥tesis seleccionada");
    }

    // 1. Guardar predicci√≥n en predictions/
    const predictionRef = doc(db, "predictions", draft.userId);

    await setDoc(predictionRef, {
      userId: draft.userId,
      hypothesis: draft.hypothesis,
      userName: draft.userName,
      suggestedName: draft.suggestedName,
      message: draft.message,
      scores: draft.scores,
      timestamp: serverTimestamp(),
      userAgent: navigator.userAgent,
    });

    logger.log("‚úÖ Predicci√≥n guardada en predictions/");

    // 2. Actualizar estad√≠sticas globales en config/event
    await updateStats(draft);

    logger.log("üéâ ¬°Predicci√≥n enviada exitosamente!");
  } catch (error) {
    logger.error("‚ùå Error enviando predicci√≥n:", error);
    throw error;
  }
}

/**
 * Actualiza las estad√≠sticas en config/event
 */
async function updateStats(draft: PredictionDraft): Promise<void> {
  const statsRef = doc(db, "config", "event");

  // Determinar qu√© contador incrementar
  const hypothesisField =
    draft.hypothesis === "XX" ? "stats.xxCount" : "stats.xyCount";

  try {
    await updateDoc(statsRef, {
      // Incrementar contadores
      "stats.totalPredictions": increment(1),
      [hypothesisField]: increment(1),

      // Agregar solo el STRING del nombre al array
      "stats.topNames": arrayUnion(draft.suggestedName),

      // Actualizar timestamp
      "stats.lastUpdated": serverTimestamp(),
    });

    logger.log("‚úÖ Estad√≠sticas actualizadas en config/event");
  } catch (error) {
    logger.error("‚ö†Ô∏è Error actualizando stats (no cr√≠tico):", error);
  }
}
